from pygtfs.exceptions import PygtfsValidationError

def validate_phone(self):
    has_only_numbers = True
    if not re.match("^[0-9-{12}]+$",str(self.phone.data)):
        has_only_numbers =False
        raise ValueError('Phone number must be in a corrrect format')
    return has_only_numbers

class ShowForm(Form):
    artist_id = StringField(
        'artist_id'
    )
    venue_id = StringField(
        'venue_id'
    )
    start_time = DateTimeField(
        'start_time',
        validators=[DataRequired()],
        default= datetime.today()
    )
    def validate(self):
        is_not_null = True
        if self.start_time is None or self.start_time == ' ':
            is_not_null =False
            raise PygtfsValidationError('Empty value not allowed in {}').format(self.__getattribute__)
        return is_not_null

